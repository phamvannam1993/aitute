                                <input type="checkbox" :checked="checkedVideo" @change="selectVideo()" class="absolute rounded-full checked:bg-[#FFA411] checked:ring-1 checked:ring-[#FFA411] checked:hover:bg-[#FFA411] ring-offset-2 ring-offset-white z-50" />
