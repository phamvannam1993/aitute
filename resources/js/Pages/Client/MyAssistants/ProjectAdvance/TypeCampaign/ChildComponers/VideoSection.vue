                            <video controls v-if="videoRef" :src="videoRef" alt="image generated by AI" class="w-auto object-cover max-h-video h-video" :class="classVideo" />
                            <video controls v-else :src="previewVideoUpload" alt="image generated by AI" class="w-auto object-cover max-h-video h-video" :class="classVideo" />

                            <button type="button" @click="removeVideo()" class="absolute bottom-2 right-2 bg-red-500 text-white w-6 h-6 rounded-full">x</button>
